//////////////////////////////////////////////////
// Dependency
//////////////////////////////////////////////////
const mongoose = require("./connection")

//////////////////////////////////////////////////
// Model
//////////////////////////////////////////////////
const {Schema, model} = mongoose;

const pillsSchema = new Schema({
    name: String,
    description: String,
    color: String,
    shape: String,
    dosage: Number,
    beforeFood: Boolean,
    afterFood: Boolean,
    username: String,
});

const Pill = model("Pill", pillsSchema);

//////////////////////////////////////////////////
// Export Model
//////////////////////////////////////////////////
module.exports = Pill;